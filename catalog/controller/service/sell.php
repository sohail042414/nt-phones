		$this->load->model('service/sell_rate');

		$params = $this->request->post;

		$data = $this->model_service_sell_rate->getMobilePrice($params);
		
		if($data){

			print_r(json_encode(array(
				'price_number' => $data['price'],
				'price' => $this->currency->format($data['price'], $this->session->data['currency']),
				'status' => true,
				'sell_rate_id' => $data['sell_rate_id']
			)));

